namespace ContactManager.Enums 
{
    /// <summary>
    /// Definiert die verschiedenen Stufen oder Ränge in einem Hierarchiesystem.
    /// </summary>
    /// <remarks>
    /// Die <see cref="CadreLevel"/>-Enum stellt verschiedene Hierarchieebenen dar, die in einem organisatorischen oder strukturellen Kontext verwendet werden können. 
    /// Die Werte reichen von <c>Zero</c> (0) bis <c>Five</c> (5) und repräsentieren aufsteigende Stufen oder Ränge.
    /// </remarks>
    public enum CadreLevel
    {
        /// <summary>
        /// Repräsentiert die niedrigste Stufe oder den niedrigsten Rang.
        /// </summary>
        Zero = 0,

        /// <summary>
        /// Repräsentiert die erste Stufe oder den ersten Rang.
        /// </summary>
        One = 1,

        /// <summary>
        /// Repräsentiert die zweite Stufe oder den zweiten Rang.
        /// </summary>
        Two = 2,

        /// <summary>
        /// Repräsentiert die dritte Stufe oder den dritten Rang.
        /// </summary>
        Three = 3,

        /// <summary>
        /// Repräsentiert die vierte Stufe oder den vierten Rang.
        /// </summary>
        Four = 4,

        /// <summary>
        /// Repräsentiert die fünfte Stufe oder den fünften Rang.
        /// </summary>
        Five = 5
    }
}
